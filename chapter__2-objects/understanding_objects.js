///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Custom Objects: 
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// by defining your own constructors and using the new keyword. 
// These objects can have custom properties and methods.
console.log(`===============================================================================`);
function Car(make, model) {
    this.make = make;
    this.model = model;
    this.start = function () {
        console.log(`${this.make} ${this.model} is starting.`);
    };
}
const myCar = new Car('Toyota', 'Camry');
console.log(myCar.start())


